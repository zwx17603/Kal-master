-(NSString *)getLunarSpecialDate:(int)iYear Month:(int)iMonth  Day:(int)iDay  {

    NSArray *chineseDays=[NSArray arrayWithObjects:
            @"小寒", @"大寒", @"立春", @"雨水", @"惊蛰", @"春分",

            @"清明", @"谷雨", @"立夏", @"小满", @"芒种", @"夏至",

            @"小暑", @"大暑", @"立秋", @"处暑", @"白露", @"秋分",

            @"寒露", @"霜降", @"立冬", @"小雪", @"大雪", @"冬至", nil];


    int array_index = (iYear - START_YEAR)*12+iMonth -1 ;


    int64_t flag = gLunarHolDay[array_index];
    int64_t day;

    if(iDay <15)
        day= 15 - ((flag>>4)&0x0f);
    else
        day = ((flag)&0x0f)+15;

    int index = -1;

    if(iDay == day){
        index = (iMonth-1) *2 + (iDay>15? 1: 0);
    }

    if ( index >= 0  && index < [chineseDays count] ) {
        return [chineseDays objectAtIndex:index];
    } else {
        return nil;
    }

}

- (void)resetState {
    // realign to the grid
    CGRect frame = self.frame;
    frame.origin = origin;
    frame.size = kTileSize;
    self.frame = frame;

    [date release];
    date = nil;
    flags.type = KalTileTypeRegular;
    flags.highlighted = NO;
    flags.selected = NO;
    flags.marked = NO;
}

- (void)setDate:(KalDate *)aDate {
    if (date == aDate)
        return;

    [date release];
    date = [aDate retain];

    [self setNeedsDisplay];
}

- (BOOL)isSelected {
    return flags.selected;
}

- (void)setSelected:(BOOL)selected {
    if (flags.selected == selected)
        return;

    // workaround since I cannot draw outside of the frame in drawRect:
    if (![self isToday]) {
        CGRect rect = self.frame;
        if (selected) {
            rect.origin.x--;
            rect.size.width++;
            rect.size.height++;
        } else {
            rect.origin.x++;
            rect.size.width--;
            rect.size.height--;
        }
        self.frame = rect;
    }

    flags.selected = selected;
    [self setNeedsDisplay];
}

- (BOOL)isHighlighted {
    return flags.highlighted;
}

- (void)setHighlighted:(BOOL)highlighted {
    if (flags.highlighted == highlighted)
        return;

    flags.highlighted = highlighted;
    [self setNeedsDisplay];
}

- (BOOL)isMarked {
    return flags.marked;
}

- (void)setMarked:(BOOL)marked {
    if (flags.marked == marked)
        return;

    flags.marked = marked;
    [self setNeedsDisplay];
}

- (KalTileType)type {
    return flags.type;
}

- (void)setType:(KalTileType)tileType {
    if (flags.type == tileType)
        return;

    // workaround since I cannot draw outside of the frame in drawRect:
    CGRect rect = self.frame;
    if (tileType == KalTileTypeToday) {
        rect.origin.x--;
        rect.size.width++;
        rect.size.height++;
    } else if (flags.type == KalTileTypeToday) {
        rect.origin.x++;
        rect.size.width--;
        rect.size.height--;
    }
    self.frame = rect;

    flags.type = tileType;
    [self setNeedsDisplay];
}

- (BOOL)isToday {
    return flags.type == KalTileTypeToday;
}

- (BOOL)belongsToAdjacentMonth {
